<?php
$num = 1;
while ($num <= 10) {
    echo $num . " ";
    $num++;
}
echo "\n";



$number = 5;
$factorial = 1;
while ($number > 1) {
    $factorial *= $number;
    $number--;
}
echo "factorial number 5: " . $factorial . "\n";


$numb = 2;
while ($numb <= 20) {
    echo $numb . " ";
    $numb += 2;
